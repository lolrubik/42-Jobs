#include "libft.h"

int	ft_toupper(int c)
{
	if (c <= 90 && c >= 65)
		return (c);
	if (c <= 122 && c >= 97)
	{
		return (c - 32);
	}
	return (0);
}

int main()
{
	char c = 32;
	c = (char)ft_toupper((int)c);
    printf("%c", c);
    return (0);
}
